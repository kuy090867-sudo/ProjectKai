        /// <summary>
        /// stageName("1-1"~"3-3")으로 해당 맵 데이터를 반환한다.
        /// </summary>
        public static string[] GetMap(string stageName)
        {
            return stageName switch
            {
                "1-1" => Stage1_1,
                "1-2" => Stage1_2,
                "1-3" => Stage1_3,
                "2-1" => Stage2_1,
                "2-2" => Stage2_2,
                "2-3" => Stage2_3,
                "3-1" => Stage3_1,
                "3-2" => Stage3_2,
                "3-3" => Stage3_3,
                _ => null,
            };
        }

        /// <summary>
        /// 챕터별 배경 테마를 반환한다.
        /// Chapter 1: Caves / Chapter 2: SnowyMountains / Chapter 3: DeadForest
        /// </summary>
        public static string GetBackgroundTheme(string stageName)
        {
            if (string.IsNullOrEmpty(stageName) || stageName.Length < 1)
                return "Caves";

            return stageName[0] switch
            {
                '1' => "Caves",
                '2' => "SnowyMountains",
                '3' => "DeadForest",
                _ => "Caves",
            };
        }

        /// <summary>
        /// 챕터별 배너 색상을 반환한다.
        /// Chapter 1: red / Chapter 2: blue / Chapter 3: red
        /// </summary>
        public static string GetBannerColor(string stageName)
        {
            if (string.IsNullOrEmpty(stageName) || stageName.Length < 1)
                return "red";

            return stageName[0] switch
            {
                '1' => "red",
                '2' => "blue",
                '3' => "red",
                _ => "red",
            };
        }
